    public ArrayList<User1> userList(){
        ArrayList<User1> usersList = new ArrayList<>();
       try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url="jdbc:sqlserver://localhost:1433;databaseName=asmBank;integratedSecurity=true;";
            Connection con = DriverManager.getConnection(url);
            String Sql1="select * from withdraw";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(Sql1);
            User1 user;
            while(rs.next()){
                user =new User1(rs.getString("accountNumber"),rs.getString("accountName"),rs.getString("date1"),rs.getString("debitBalance"));
                usersList.add(user);
            }         
       }
       
       
       catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
       return usersList;
    }
    public void showUser(){
        DefaultTableModel model =( DefaultTableModel ) withdrawTable.getModel();
        ArrayList<User1> list = userList();
        
        Object[] row = new Object[5];
        for(int i=0;i<list.size();i++){
            row[0]=list.get(i).getA();
            row[1]=list.get(i).getB();
            row[2]=list.get(i).getC();
            row[3]=list.get(i).getD();
            model.addRow(row);
        }
    }
    
